#include "libft.h"

size_t		ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	len_dst;
	size_t	len_src;

	len_dst = ft_strlen(dst);
	len_src = ft_strlen(src);
	if (len_dst < size)
	{
		dst = ft_strncat(dst, src, size - len_dst - 1);
		return (len_src + len_dst);
	}
	else
		return (len_src + size);
}
